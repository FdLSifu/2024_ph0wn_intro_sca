unsigned char hw(unsigned char a)
{
    unsigned char h = 0;
    for (int i = 0; i < 8; i++)
    {
        h += a & 1;
        a >>= 1;
    }
    return h;
}

void leak(char *trace, unsigned char b, unsigned char factor)
{
    unsigned char noise = (unsigned char)(rand() & 0x7F);

    *trace = (char)(factor * hw(b) + noise);

    return;
}

void leak2(char *trace, unsigned char b, unsigned char factor)
{
    unsigned char noise = (unsigned char)(rand() & 0xF);

    *trace = (char)(factor * hw(b) + noise);

    return;
}

int rsa_withleakage(const char *msg, char *trace)
{

    char *ctrace = trace;
    // Initialize GMP variables
    mpz_t m, c, n;
    mpz_inits(m, c, n, NULL);

    // Import modulus from unsigned char array
    mpz_import(n, sizeof(modulus), 1, sizeof(modulus[0]), 0, 0, modulus);
    for (int i = 0; i < sizeof(modulus); i++)
        leak(ctrace++, modulus[i], 10);

    // Convert message to GMP integer
    mpz_import(m, MAX_SIZE, 1, sizeof(msg[0]), 0, 0, msg);
    for (int i = 0; i < MAX_SIZE; i++)
        leak(ctrace++, msg[i], 10);

    // Perform modular exponentiation (c = m^d mod n) square and multiply
    mpz_set_ui(c, 1);
    leak(ctrace++, 1, 10);

    for (int i = 0; i < sizeof(private); i++)
    {
        for (int b = 7; b >= 0; b--)
        {
            for (int l = 0; l < 128; l++)
                leak(ctrace++, 10, 10);
            // Square
            mpz_mul(c, c, c);
            for (int l = 0; l < mpz_size(c); l++)
                leak(ctrace++, c->_mp_d[0], 10);

            mpz_mod(c, c, n);
            for (int l = 0; l < mpz_size(n); l++)
                leak(ctrace++, n->_mp_d[0], 10);

            if (private[i] & (1 << b))
            {
                for (int l = 0; l < 128; l++)
                    leak2(ctrace++, 10, 1);

                // multiply
                mpz_mul(c, c, m);
                for (int l = 0; l < mpz_size(c); l++)
                    leak2(ctrace++, c->_mp_d[0], 1);
                mpz_mod(c, c, n);
                for (int l = 0; l < mpz_size(n); l++)
                    leak2(ctrace++, n->_mp_d[0], 1);
            }
        }
    }
    // Clean up GMP variables
    mpz_clears(m, c, n, NULL);

    return (int)(ctrace - trace);
}